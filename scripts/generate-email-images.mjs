import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, "..");
const publicDir = path.join(root, "public");
const outputFile = path.join(root, "src/lib/mail/email-images.generated.ts");

const EMAIL_AIRLINE_ASSETS = [
  { name: "Air India", file: "logos/Air_India_Logo.svg.png" },
  { name: "Emirates", file: "logos/Emirates_logo.svg.png" },
  {
    name: "Qatar Airways",
    file: "logos/634084-QR-Logo-Full-Colour-Horizontal-bd9477-original-1765966477.jpg",
  },
  { name: "British Airways", file: "logos/British_Airways_Logo.svg.png" },
  { name: "KLM", file: "logos/KLM_logo.svg.png" },
  { name: "Air Canada", file: "logos/Air_Canada-Logo.wine.svg" },
  { name: "Thai Airways", file: "logos/Thai_Airways_logo.svg.png" },
  { name: "EgyptAir", file: "logos/egyptair-logo-png_seeklogo-46182.png" },
  { name: "Garuda Indonesia", file: "logos/images.png" },
];

const MIME_TYPES = {
  ".png": "image/png",
  ".jpg": "image/jpeg",
  ".jpeg": "image/jpeg",
  ".svg": "image/svg+xml",
};

function toDataUri(relativePath) {
  const assetPath = path.join(publicDir, relativePath);
  const ext = path.extname(relativePath).toLowerCase();
  const mimeType = MIME_TYPES[ext] ?? "application/octet-stream";
  const content = fs.readFileSync(assetPath);
  return `data:${mimeType};base64,${content.toString("base64")}`;
}

const brandLogo = toDataUri("dummy-logo-icon.png");
const airlines = EMAIL_AIRLINE_ASSETS.map((airline) => ({
  name: airline.name,
  src: toDataUri(airline.file),
}));

const fileContents = `/* eslint-disable */
// Auto-generated by scripts/generate-email-images.mjs — do not edit manually.

export type GeneratedEmailImageData = {
  brandLogo: string;
  airlines: readonly { name: string; src: string }[];
};

export const GENERATED_EMAIL_IMAGES: GeneratedEmailImageData = ${JSON.stringify(
  { brandLogo, airlines },
  null,
  2
)};
`;

fs.writeFileSync(outputFile, fileContents);
console.log(`Generated ${outputFile}`);
